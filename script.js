var id;

function find_song(){
    nome = (document.getElementById('search').value).toLowerCase();

    for (let i = 0; i < songs.length; i++) {

        if((songs[i][0]).includes(nome) && nome != ""){

            document.getElementById('song').style.display = 'block';
            document.getElementById('cover').style.backgroundImage = songs[i][1];
            document.getElementById('song_name').innerHTML = songs[i][2];

            id = i;
            break;

        }else if(i == 4){
            document.getElementById('song').style.display = 'none';
            alert('Música não encontrada');
        }
    }
}

function show_player(){
    document.getElementById('home').style.display = 'none';
    document.getElementById('player').style.display = 'block';

    document.getElementById('album_cover').style.backgroundImage = songs[id][1];
    document.getElementById('song_name_player').innerHTML = songs[id][2];
    document.getElementById('audio_player').src = songs[id][3];
    document.getElementById('audio_player').play();
    document.getElementById('letra').innerHTML = "";
    document.getElementById('letra').innerHTML = letras[id];
}

function hide_player(){
    document.getElementById('home').style.display = 'block';
    document.getElementById('player').style.display = 'none';
    document.getElementById('audio_player').pause();
}
var status = 'play';

function play_pause(){

    if(status == 'play'){
        document.getElementById('audio_player').pause();
        status = 'pause';
        document.getElementById('bt2').style.backgroundImage = "url('img/play.svg')";

    }else if(status == 'pause'){
        document.getElementById('audio_player').play();
        document.getElementById('bt2').style.backgroundImage = "url('img/pause.svg')";
        status = 'play';
    }
}

